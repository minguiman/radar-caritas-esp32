inline void glyphFor2x(char c, uint16_t* outBuffer)
{
    const uint8_t* src = glyphFor(c);
    for (int row = 0; row < 7; ++row) {
        uint8_t srcRow = src[row];
        uint16_t dstRow = 0;
        for (int col = 0; col < 5; ++col) {
            if ((srcRow >> (4 - col)) & 0x01) {
                dstRow |= (3 << ((4 - col) * 2));
            }
        }
        outBuffer[row * 2] = dstRow;
        outBuffer[row * 2 + 1] = dstRow;
    }
}
